// 预编译正则：避免重复创建
export function buildLatexRegex(keywords: string[]): RegExp {
  const escaped = keywords
    .filter(k => k.length >= 2)
    .map(k => k.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'));
  return new RegExp(`(${escaped.join('|')})`, 'g');
}